package commons.utils;

public class Datetime {
    public int year;
    public int month;
    public int day;
    public int hour;
    public int minute;

    public Datetime(int year, int month, int day, int hour, int minute) {
        this.year = year;
        this.month = month;
        this.day = day;
        this.hour = hour;
        this.minute = minute;
    }
}
